// 総当たりでナルシスト数を探す

// a^3 + b^3 + c^3 =
// a * 100 + b * 10 + c

const t = 2

for (let a = 10 ** t; a < 10 ** (t + 1); a++) {
  for (let b = 10 ** t; b < 10 ** (t + 1); b++) {
    for (let c = 10 ** t; c < 10 ** (t + 1); c++) {
      if (
        a ** 3 + b ** 3 + c ** 3 ===
        a * 100 ** (t + 1) + b * 10 ** (t + 1) + c
      ) {
        console.log(`${a}${b}${c} = ${a ** 3} + ${b ** 3} + ${c ** 3}`)
      }
    }
  }
}
